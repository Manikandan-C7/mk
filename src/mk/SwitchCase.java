package mk;

public class SwitchCase {

	public static void main(String[] args) {
	
		int days=1;
		
	    switch (days) {
	    case 1:
	    case 2:
	    case 3:	
	    case 4:
	    case 5:
	    	System.out.println("WEEKDAYS");
	    	break;
	    case 6:
	    case 7:
	    	System.out.println("WEEKEND");
	    	break;
	    	

	    }
}
}
