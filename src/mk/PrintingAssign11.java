package mk;

public class PrintingAssign11 {

	public static void main(String[] args) {
		//PRINTING ONLY MULTIPLIES OF 3 AND 5
             for(int i=1;i<=15;i++) {
            	 if(i%3==0) {
            		 System.out.println(i);
            	 }
             }
             for(int j=1;j<=25;j++) {
            	 if(j%5==0) {
            		 System.out.println(j);
            	 }
             }
	}

}
